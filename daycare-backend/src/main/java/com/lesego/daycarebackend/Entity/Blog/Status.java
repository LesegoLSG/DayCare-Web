package com.lesego.daycarebackend.Entity.Blog;
/**
 * Enumeration representing the status of a blog post.
 * CURRENT_POST: Represents a blog post that is currently active and published.
 * UPCOMING_POST: Represents a blog post that is scheduled to be published in the future.
 * @author Mhlongo Lesego
 */
public enum Status {
    CURRENT_POST,
    UPCOMING_POST
}
